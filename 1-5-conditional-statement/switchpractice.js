

let day = `목요일`;

switch(day)
{
    case `월요일` :
        console.log(`**========`);
        console.log(`한주를 시작합니다`);
        break;
    case `화요일` :
        console.log(`****======`);
        console.log(`맛있는걸 먹읍시다`);
        break;
    case `수요일` :
        console.log(`******====`);
        console.log(`절반이 지나고 있어요!`);
        break;
    case `목요일` :
        console.log(`********==`);
        console.log(`주말이 다 와 갑니다`);
        break;
    case `금요일` :
        console.log(`**********`);
        console.log(`주말이 코앞입니다`);
        break;
    default:
        console.log(`푹쉬세요!`);
}